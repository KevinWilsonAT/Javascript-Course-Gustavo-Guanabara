//   Relational Operators

//   >     Greater
//   <     Lesser
//   >=    Greater or equal
//   <=    Lesser or equal
//   ==    Equal
//   !=    Different

//   5 >  2     true
//   7 <  4     false
//   8 >= 8     true
//   9 <= 7     false
//   5 == 5     true
//   4 != 4     false

// Identity

//   5 == 5     true
//   5 == '5'   true
//   5 === '5'  false
//   5 === 5    true

//   5 != '5'   false
//   5 !== '5'  true